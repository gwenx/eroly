package com.eroly.domain;

public class Permission {
    private Integer permissionRole;

    private Integer permissionFuns;

    public Integer getPermissionRole() {
        return permissionRole;
    }

    public void setPermissionRole(Integer permissionRole) {
        this.permissionRole = permissionRole;
    }

    public Integer getPermissionFuns() {
        return permissionFuns;
    }

    public void setPermissionFuns(Integer permissionFuns) {
        this.permissionFuns = permissionFuns;
    }
}